package com.creativepalace.model;

public class Student {
	private Long studentID;
	private String studentName;
	private String studentEmail;
	private String studentPassword;
	private String studentPhone;
	private String studentPhoto;
	private String studentHome;
	private String studentStreet;
	private String studentTownship;
	private String studentCity;

	public String getStudentName() {
		return studentName;
	}

	public void setStudentName(String studentName) {
		this.studentName = studentName;
	}

	public Long getStudentID() {
		return studentID;
	}

	public void setStudentID(Long studentID) {
		this.studentID = studentID;
	}

	public String getStudentEmail() {
		return studentEmail;
	}

	public void setStudentEmail(String studentEmail) {
		this.studentEmail = studentEmail;
	}

	public String getStudentPassword() {
		return studentPassword;
	}

	public void setStudentPassword(String studentPassword) {
		this.studentPassword = studentPassword;
	}

	public String getStudentPhone() {
		return studentPhone;
	}

	public void setStudentPhone(String studentPhone) {
		this.studentPhone = studentPhone;
	}

	public String getStudentPhoto() {
		return studentPhoto;
	}

	public void setStudentPhoto(String studentPhoto) {
		this.studentPhoto = studentPhoto;
	}

	public String getStudentHome() {
		return studentHome;
	}

	public void setStudentHome(String studentHome) {
		this.studentHome = studentHome;
	}

	public String getStudentStreet() {
		return studentStreet;
	}

	public void setStudentStreet(String studentStreet) {
		this.studentStreet = studentStreet;
	}

	public String getStudentTownship() {
		return studentTownship;
	}

	public void setStudentTownship(String studentTownship) {
		this.studentTownship = studentTownship;
	}

	public String getStudentCity() {
		return studentCity;
	}

	public void setStudentCity(String studentCity) {
		this.studentCity = studentCity;
	}
}
